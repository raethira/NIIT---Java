//abstract class
abstract class vech
{
	abstract void drive();	 //declare
	public void lightson()	  //definie
	{
		System.out.println("lights on called");
	}
}
class car extends vech
{
	public void drive()		 //override
	{
		System.out.println("drive called for car");
	}
}
class bike extends vech
{
	public void drive()
	{
		System.out.println("dibve calledfor bike");
	}
}
class vechdemo
{
	public static void main(String kat[])
	{
		bike b=new bike();
			b.drive();
		car c=new car();
			c.drive();
		b.lightson();
	}
}

