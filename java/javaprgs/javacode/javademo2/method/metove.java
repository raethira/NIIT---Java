//method overloading 
class metove
{
	int  add(int i,int j)
	{
		return i+j;
	}
	int  add(int i,int j,int k)
	{
		return i+j+k;
	}
	double  add(double i,int j,float k)
	{
		return i+j+k;
	}
	void add()
	{
		System.out.println("welcone");
	}
	String add(String str)
	{
		return str;
	}
	public static void main(String[] args) 
	{
		metove a=new metove();
		a.add();
		System.out.println(a.add(32,78));
		System.out.println(a.add(23f,78,45f));
		System.out.println(a.add(23,78,45));
		System.out.println(a.add("kalala"));
	}
}
