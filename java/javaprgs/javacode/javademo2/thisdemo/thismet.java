class thismet 
{
	int i,j;
	String str;
	int add(int i,int j)
	{
		this.i=i;
		this.j=j;
		return i+j;
	}
	String display(String str)
	{
		this.str=str;
		return str;
	}
 	public static void main(String[] args) 
	{
		thismet obj=new thismet();
		System.out.println(obj.add(12,34));
		System.out.println(obj.display("naga"));
		System.out.println("Hello World!");
	}
}
