import java.io.*;
class filereader 
{
	public static void main(String[] args) 
	{
		try
		{
			FileInputStream f=new FileInputStream("ab.java");
			FileReader fr=new FileReader(f);
			int ch;
			while((int)ch==fr.read()!=-1)
			{
				System.out.println((char)ch);
			}
		}
		catch(Exception e)
			{
				System.out.println("Exception:"+e);
			}
		}
	}

